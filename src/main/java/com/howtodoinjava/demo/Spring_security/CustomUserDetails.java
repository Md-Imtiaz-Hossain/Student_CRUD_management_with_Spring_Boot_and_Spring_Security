package com.howtodoinjava.demo.Spring_security;


import com.howtodoinjava.demo.model.StudentEntity;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.Collection;

public class CustomUserDetails implements UserDetails {

      private StudentEntity studentEntity;

    public CustomUserDetails(StudentEntity studentEntity) {
        this.studentEntity = studentEntity;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return null;
    }

    @Override
    public String getPassword() {
        return studentEntity.getUsername();
    }

    @Override
    public String getUsername() {
        return studentEntity.getEmail();
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }
}
